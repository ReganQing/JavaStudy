package Day2;

public class DataTypeDemo1 {
    //主入口
    public static void main(String[] args){
        //定义变量记录姓名
        String name = "Ron";
        //定义变量记录年龄
        int age = 24;
        //定义变量记录性别
        char gender = '男';
        //定义变量记录身高
        double height = 183.3;
        //定义变量是否在家
        //true 在家 false 外出
        boolean flag = true;
        //输出变量的值
        System.out.println(name);
        System.out.println(age);
        System.out.println(gender);
        System.out.println(height);
        System.out.println(flag);

    }
}
