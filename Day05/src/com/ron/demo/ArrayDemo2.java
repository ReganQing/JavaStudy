package com.ron.demo;

public class ArrayDemo2 {
    public static void main(String[] args) {
        //利用索引访问数组

        //1.获取数组里面的元素
        //  格式：数组名[索引]

        int[] arr = {1,2,3,4,5};
        //获取数组中的第一个元素其实就是0索引上对应的元素
        int number = arr[0];
        System.out.println(number);

        //2.把数据存储到数组当中
        //  格式：  数组名[索引] = 具体数据/变量;
    }
}
